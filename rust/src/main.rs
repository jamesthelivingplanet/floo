fn cmd_list(json: bool, db_override: Option<&str>) -> i32 {
    let conn = match open_db(db_override) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };
    let claims = match registry::list_claims(&conn) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };
    if json {
        let entries: Vec<ListEntryJson> = claims
            .iter()
            .map(|c| ListEntryJson {
                claim: c,
                listening: !scanner::is_port_free_on_os(c.port),
            })
            .collect();
        println!("{}", serde_json::to_string_pretty(&entries).unwrap());
        return 0;
    }
    if claims.is_empty() {
        println!("No claims yet. Run `floo claim <service>` in a repo to make one.");
        return 0;
    }
    println!("{:<6} {:<10} {:<14} REPO", "PORT", "LISTENING", "SERVICE");
    for c in &claims {
        let listening = if scanner::is_port_free_on_os(c.port) {
            "no"
        } else {
            "yes"
        };
        println!(
            "{:<6} {:<10} {:<14} {}",
            c.port, listening, c.service, c.repo_path
        );
    }
    0
}

fn cmd_claim(
    service: Option<String>,
    prefer: Option<u16>,
    json: bool,
    db_override: Option<&str>,
) -> i32 {
    let Some(service) = service else {
        return print_claim_usage_with_state(db_override);
    };

    let rp = match current_repo_path() {
        Ok(rp) => rp,
        Err(e) => return fail(&e),
    };
    let conn = match open_db(db_override) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };
    match registry::claim(&conn, &rp, &service, prefer) {
        Ok(result) => {
            if json {
                let out = ClaimJson {
                    claim: &result.claim,
                    was_new: result.was_new,
                };
                println!("{}", serde_json::to_string_pretty(&out).unwrap());
            } else {
                println!("{}", result.claim.port);
            }
            0
        }
        Err(e) => fail(&e),
    }
}

fn cmd_release(service: Option<String>, all: bool, db_override: Option<&str>) -> i32 {
    let conn = match open_db(db_override) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };

    if all {
        return match registry::release_all(&conn) {
            Ok(n) => {
                println!("Released {n} claim(s).");
                0
            }
            Err(e) => fail(&e),
        };
    }

    let Some(service) = service else {
        return print_release_usage_with_state(db_override);
    };

    let rp = match current_repo_path() {
        Ok(rp) => rp,
        Err(e) => return fail(&e),
    };

    match registry::release(&conn, &rp, &service) {
        Ok(true) => {
            println!("Released {service}.");
            0
        }
        Ok(false) => {
            eprintln!("No claim for service '{service}' in this repo.");
            1
        }
        Err(e) => fail(&e),
    }
}

fn cmd_gc(older_than: &str, dry_run: bool, db_override: Option<&str>) -> i32 {
    let conn = match open_db(db_override) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };
    let cands = match registry::gc(&conn, older_than, dry_run) {
        Ok(c) => c,
        Err(e) => return fail(&e),
    };
    if cands.is_empty() {
        println!("Nothing to reclaim.");
        return 0;
    }
    let verb = if dry_run {
        "Would reclaim"
    } else {
        "Reclaimed"
    };
    for c in &cands {
        println!(
            "{verb}: port {} ({} @ {}) - {}",
            c.claim.port, c.claim.service, c.claim.repo_path, c.reason
        );
    }
    0
}

fn cmd_agent_setup() -> i32 {
    let target = match agent_setup::claude_md_path() {
        Ok(p) => p,
        Err(e) => {
            eprintln!("{e}");
            return 1;
        }
    };
    match agent_setup::install(&target) {
        Ok((path, action)) => {
            let mut chars = action.chars();
            let capitalized = match chars.next() {
                Some(first) => first.to_uppercase().collect::<String>() + chars.as_str(),
                None => action,
            };
            println!("{capitalized} floo block in {}", path.display());
            0
        }
        Err(e) => {
            eprintln!("{e}");
            1
        }
    }
}

fn cmd_completions(shell: Shell) -> i32 {
    let mut cmd = Cli::command();
    clap_complete::generate(shell, &mut cmd, "floo", &mut io::stdout());
    0
}

