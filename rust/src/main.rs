const VERSION: &str = env!("CARGO_PKG_VERSION");

/// The top-level help block. Kept as a static string so `floo`, `floo -h`, and
/// `floo --help` all render the exact same tailored overview.
const TOP_HELP: &str = "usage: floo <command> [options]

commands:
  version                Print floo version
  list                   Show all claims and listening status
                           --json
  claim <service>        Claim (or fetch) a port for a service
                           --prefer <port>
                           --json
  release <service>      Release a claim
  release --all          Release every claim
  gc                     Reclaim stale claims
                           --older-than <duration> (default '-7 days')
                           --dry-run
  agent-setup            Write the floo instruction into ~/.claude/CLAUDE.md
  completions <shell>    Print a shell completion script (bash, zsh, fish)

options:
  --db <path>            Use this registry file instead of the XDG default
  --version, -V          Print version and exit
  -h, --help             Show this help

The registry path is resolved as: --db <path> if given, else the FLOO_DB
environment variable if set, else the XDG default
($XDG_STATE_HOME/floo/registry.db or ~/.local/state/floo/registry.db).";

/// JSON shape emitted by `claim --json`: the claim record plus `was_new`.
#[derive(Serialize)]
struct ClaimJson<'a> {
    #[serde(flatten)]
    claim: &'a registry::Claim,
    was_new: bool,
}

/// JSON shape emitted by `list --json`: each claim record plus live
/// `listening` status observed at print time.
#[derive(Serialize)]
struct ListEntryJson<'a> {
    #[serde(flatten)]
    claim: &'a registry::Claim,
    listening: bool,
}

/// Top-level CLI. Per-subcommand `--help` is generated by clap; the top-level
/// help is overridden with `TOP_HELP` at parse time.
#[derive(Parser)]
#[command(name = "floo", version, disable_help_subcommand = true)]
struct Cli {
    /// Use this registry file instead of the XDG default.
    #[arg(long, global = true, value_name = "path")]
    db: Option<String>,

    #[command(subcommand)]
    command: Option<Command>,
}

#[derive(Subcommand)]
enum Command {
    /// Claim (or fetch) a port for a service.
    Claim {
        /// Service label, for example web, api, storybook.
        service: Option<String>,
        /// Preferred port to try first.
        #[arg(long, value_name = "port")]
        prefer: Option<u16>,
        /// Emit the claim as JSON instead of a bare port.
        #[arg(long)]
        json: bool,
    },
    /// Release a claim, or every claim with --all.
    Release {
        /// Service label to release.
        service: Option<String>,
        /// Release every claim in the registry.
        #[arg(long)]
        all: bool,
    },
    /// Show all claims and listening status.
    List {
        /// Emit the claim list as JSON.
        #[arg(long)]
        json: bool,
    },
    /// Reclaim stale claims.
    Gc {
        /// SQLite datetime modifier, for example '-7 days'.
        #[arg(long, value_name = "duration", default_value = "-7 days")]
        older_than: String,
        /// Show what would be reclaimed without changing anything.
        #[arg(long)]
        dry_run: bool,
    },
    /// Write the floo instruction into ~/.claude/CLAUDE.md.
    AgentSetup,
    /// Print floo version.
    Version,
    /// Print a shell completion script (bash, zsh, fish).
    Completions {
        /// Shell to generate completions for.
        shell: Shell,
    },
}

fn main() -> ExitCode {
    // Override only the root command's help so `floo`, `floo -h`, and
    // `floo --help` render TOP_HELP, while each subcommand keeps its own
    // clap-generated, tailored help.
    let mut cmd = Cli::command().override_help(TOP_HELP);
    let matches = match cmd.try_get_matches_from_mut(std::env::args_os()) {
        Ok(m) => m,
        Err(err) => return handle_clap_error(err),
    };
    let cli = Cli::from_arg_matches(&matches).expect("clap matches should deserialize into Cli");
    ExitCode::from(run(cli) as u8)
}

/// Translate a clap parse error into floo's exit-code and message contract.
fn handle_clap_error(err: clap::Error) -> ExitCode {
    match err.kind() {
        // Help and version are not errors: clap renders them to stdout, exit 0.
        ErrorKind::DisplayHelp | ErrorKind::DisplayVersion => {
            let _ = err.print();
            ExitCode::from(0)
        }
        // Preserve the historical "Unknown command: <name>" message and exit 2.
        ErrorKind::InvalidSubcommand => {
            eprintln!("Unknown command: {}", invalid_subcommand_name(&err));
            ExitCode::from(2)
        }
        // Any other usage error keeps clap's own message and exit code.
        _ => {
            let _ = err.print();
            ExitCode::from(err.exit_code() as u8)
        }
    }
}

/// Extract the offending subcommand token from a clap InvalidSubcommand error.
fn invalid_subcommand_name(err: &clap::Error) -> String {
    match err.get(ContextKind::InvalidSubcommand) {
        Some(ContextValue::String(s)) => s.clone(),
        _ => String::new(),
    }
}

fn run(cli: Cli) -> i32 {
    let db_override = cli.db.as_deref();
    match cli.command {
        None => {
            println!("{TOP_HELP}");
            0
        }
        Some(Command::List { json }) => cmd_list(json, db_override),
        Some(Command::Claim {
            service,
            prefer,
            json,
        }) => cmd_claim(service, prefer, json, db_override),
        Some(Command::Release { service, all }) => cmd_release(service, all, db_override),
        Some(Command::Gc {
            older_than,
            dry_run,
        }) => cmd_gc(&older_than, dry_run, db_override),
        Some(Command::AgentSetup) => cmd_agent_setup(),
        Some(Command::Version) => {
            println!("floo {VERSION}");
            0
        }
        Some(Command::Completions { shell }) => cmd_completions(shell),
    }
}

fn open_db(db_override: Option<&str>) -> Result<rusqlite::Connection, FlooError> {
    let path = paths::db_path_with_override(db_override)?;
    registry::connect(&path)
}

fn current_repo_path() -> Result<String, FlooError> {
    let p = paths::repo_root()?;
    Ok(p.to_string_lossy().into_owned())
}

